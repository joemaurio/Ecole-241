          <p>C’est à l’auditorium de la Maison Georges RAWIRI de Gabon Télévisions que le Directeur Général de la Caisse Nationale de Sécurité Sociale (CNSS), Dr Nicole ASSELE, a annoncé les mesures d’accompagnement liés à la mise en route du retour au trimestre du paiement des pensions.</p>
          <p>
Une mesure porteuse d’un certain inconfort social chez les retraités, malgré le fait qu’elle soit vitale pour l’Institution, a indiqué en substance Dr Nicole ASSELE, devant la presse nationale et internationale. Cette mesure se traduit notamment par la mise en place d’un programme d’accompagnement social, destiné à atténuer les chocs et à assurer la dignité et l’équilibre social des retraités et leurs ayants-droit.</p>
