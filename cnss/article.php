Dr Nicole ASSELE a enfin tenu à rassurer les uns et les autres quant à son silence face aux multiples réactions concernant le retour à la trimestrialisation des pensions retraites « Loin d’être un signe de mépris ou d’indifférence, il s’agissait plutôt d’accompagner sereinement la maturation dudit programme social », a-t-elle conclut.</p>
