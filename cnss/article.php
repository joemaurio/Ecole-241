            <p>
              Ce programme qui sera mis en route immédiatement après le paiement des pensions au titre du troisième trimestre 2019, à partir du 05 août prochain, se décline par la mise en place immédiate d’une carte bancaire prépayée gratuite qui permettra à tous les retraités, bancarisés ou non, de retirer mensuellement leurs pensions auprès des partenaires financiers qui eux, seront réglés trimestriellement par la CNSS. Ceci, pour rester à la fois conforme à la loi, aux contraintes de trésorerie et continuer à garantir un mieux-être aux retraités.
            </p>
            <p>Cette carte prépayée ouvrira dans le même temps aux bénéficiaires, le droit aux réductions sur les produits de consommation de première nécessité auprès des partenaires commerciaux de la CNSS, aussi bien à Libreville qu’à l’intérieur du pays.

Autre mesure annoncée par le Directeur Général de la CNSS, la prise en charge médicale gratuite, entre les 1er et 5 de chaque mois, exclusivement dans les Centres d’Action Sanitaire et Sociale (CASS) et Centres Médico-Sociaux(CMS), avec réduction des coûts des produits pharmaceutiques sur le réseau de dispensation de médicaments de la CNSS.</p>
          
